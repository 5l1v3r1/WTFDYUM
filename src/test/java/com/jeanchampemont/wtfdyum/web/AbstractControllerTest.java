package com.jeanchampemont.wtfdyum.web;

import com.jeanchampemont.wtfdyum.WTFDYUMApplication;
import org.junit.Before;
import org.junit.runner.RunWith;
import org.springframework.boot.test.SpringApplicationConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;
import org.springframework.test.context.web.WebAppConfiguration;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;

/**
 * This class should be extended by all test class testing controllers.
 *
 * @author Jean
 */
@RunWith(SpringJUnit4ClassRunner.class)
@SpringApplicationConfiguration(classes = WTFDYUMApplication.class)
@WebAppConfiguration
public abstract class AbstractControllerTest {

    /** The mock mvc. */
    protected MockMvc mockMvc;

    /**
     * Setup : - Init mockMvc.
     */
    @Before
    public void setup() {
        mockMvc = MockMvcBuilders.standaloneSetup(getTestedController()).build();
    }

    /**
     * Gets the tested controller.
     *
     * @return the tested controller
     */
    protected abstract Object getTestedController();
}
